package com.user;

public class User {

    public  int index;
    public String name;
    public int age;
    public String address;
    public String password;

    public User() {
    }

    public User(int index, String name, int age, String address, String password) {
        this.index = index;
        this.name = name;
        this.age = age;
        this.address = address;
        this.password = password;
    }
}
